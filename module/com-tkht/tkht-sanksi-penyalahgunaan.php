<p style="text-align: justify; margin-top: 15px;">Berikut adalah penjelasan sanksi penyalahgunaan TKHT.</p>
<ol type="1" style="color: #555; font-size: .99rem; margin-bottom: 22px;">
    <li>
        <p style="text-align: justify; margin-bottom: 6px;">Bentuk penyalahgunaan TKHT ditetapkan sebagai berikut : </p>
        <ol type="a">
            <li>
                <p style="text-align: justify; margin-bottom: 6px;">Menggunakan TKHT untuk keperluan orang lain yang tidak ditanggung Yayasan.</p>
            </li>
            <li>
                <p style="text-align: justify; margin-bottom: 6px;">Menjual dan / atau menukar obat dengan barang lain.</p>
            </li>
            <li>
                <p style="text-align: justify; margin-bottom: 6px;">Melakukan penipuan dalam menggunakan TKHT.</p>
            </li>
            <li>
                <p style="text-align: justify; margin-bottom: 6px;">Tidak melaporkan perubahan status Penerima TKHT yang mengakibatkan masih diberikannya TKHT.</p>
            </li>
            <li>
                <p style="text-align: justify; margin-bottom: 6px;">Penyalahgunaan lain yang merugikan Yayasan secara langsung ataupun tidak langsung.</p>
            </li>
        </ol>
    </li>
    <li><p style="text-align: justify; margin-bottom: 6px;">Penyalahgunaan TKHT dapat dikenakan sanksi berupa penggantian kerugian, dan/ atau penghentian TKHT dalam jangka waktu tertentu sampai dengan penghentian TKHT selamanya.</p></li>
    <li><p style="text-align: justify; margin-bottom: 6px;">Ketentuan dan tata cara penetapan sanksi atas penyalahgunaan TKHT sebagaimana dimaksud pada angka 1 diatur dalam Lampiran 14.</p></li>
    <li><p style="text-align: justify; margin-bottom: 6px;">Sanksi atas penyalahgunaan TKHT sebagaimana dimaksud pada angka 2 ditetapkan berdasarkan Keputusan Pengurus Yayasan.</p></li>
</ol>